function permissions(moduleId: ImpactModuleId, actions: PermissionAction[]): Partial<Record<PermissionAction, PermissionKey>> {
  return actions.reduce(
    (result, action) => ({ ...result, [action]: `${moduleId}:${action}` as PermissionKey }),
    {} as Partial<Record<PermissionAction, PermissionKey>>,
  );
}

