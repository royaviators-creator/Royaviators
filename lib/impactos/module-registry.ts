export function getModuleById(moduleId: ImpactModuleId) {
  return coreModuleRegistry.find((module) => module.id === moduleId);
}

export function listModules(moduleIds?: ImpactModuleId[]) {
  if (!moduleIds) return coreModuleRegistry;
  return coreModuleRegistry.filter((module) => moduleIds.includes(module.id));
}
